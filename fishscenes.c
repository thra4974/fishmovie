void clearScreen()
{
	const char *CLEAR = "\e[1;1H\e[2J";
	write(STDOUT_FILENO, CLEAR, 12);
}

int _putchar(char c)
{
	return (write(1, &c, 1));
}

void _puts(char *s)
{
	int i = 0;

	while (s[i] != '\0')
	{
		_putchar(s[i]);
		i++;
	}
}

